function setup() {
  createCanvas(400, 400);
  angleMode(DEGREES) //this makes working with arcs easier, since we don't all have radians and the unit circle memorized.

}

function draw() {
  background(220);
  text(mouseX + ", " + mouseY, 20,20)

  //base face shape
  arc(200,250,300,350,180,0)














}